package forloop;

import java.util.Scanner;

public class Patternprinting1_10 {

	public static void main(String[] args) {
		Scanner sc=new Scanner(System.in);
		System.out.println("enter the pattern ");
		int n=sc.nextInt();
		for(int i=1;i<=n;i++) {
		System.out.print(i);
		
		}
	}
}
